                        <div class="col-sm-12">
                            <h4 class="uppercase mb40">How to apply to a startup accelerator successfully?</h4>
                            <p class="lead">
                                Want to know how to hit the spot with your accelerator application? Here are the do’s and don't’s, the frequent mistakes and tips on how to answer the trickiest questions.
                            </p>
                            <p class="lead">
                                Once you read this not only you’ll improve your odds of getting into NUMA, chances are you’ll also have a better shot at any other startup accelerator.
                            </p>
                        </div>
                    </div>

                    <div class="row mb40">
                        <div class="col-sm-10 col-sm-offset-1">
                            <ul class="accordion accordion-2">
                                <li>
                                    <div class="title">
                                        <h4 class="inline-block mb0">Take your time</h4>
                                    </div>
                                    <div class="content">
                                        <p class="mb0">
                                            The first rule is: don’t rush it. If it’s your first time, the application questionnaire will require some serious thinking and discussion with your team: print out the questions, fire up the coffeemaker and gather your companions around the whiteboard.
                                            Evaluators can easily tell when an application has been thrown together in a haste by the CEO or copy/pasted from some generic slide deck. This is a collaborative effort, some questions require each founder
                                            to answer individually, make sure you set aside some time to work on it, we guarantee it’ll be worth it. Depending on whether you’ve applied to an accelerator before or not filling in this application may require
                                            anywhere from three hours to one week.
                                        </p>
                                    </div>
                                </li>
                                <li>
                                    <div class="title">
                                        <h4 class="inline-block mb0">English</h4>
                                    </div>
                                    <div class="content">
                                        <p>
                                            We only accept applications submitted in English as some of our evaluators do not speak other languages.
                                        </p>
                                    </div>
                                </li>
                                <li>
                                    <div class="title">
                                        <h4 class="inline-block mb0">Team, Team, Team</h4>
                                    </div>
                                    <div class="content">
                                        <h6 class="uppercase mb0 mt16">Did we say team already? Team!</h6>
                                        <p>
                                            The quality of the team is the most important factor in deciding whether we’ll be working with you or not, here’s a rundown of questions, why we ask them and tips on how to answer them.
                                        </p>
                                    </div>
                                </li>
                                <li>
                                    <div class="title">
                                        <h4 class="inline-block mb0">Pitch your startup in 140 characters or less</h4>
                                    </div>
                                    <div class="content">
                                        <p class="mb0">
                                            Here you’ll have to exercise your marketing and communication skills to get across your unique value proposition in a nutshell.
                                        </p>
                                        <p class="mb0">Example:</p>
                                        <blockquote>“We partner with water utilities to bring prepaid running water at home to poor urban families in developing nations.”</blockquote>
                                        <blockquote>“Corilla is a beautiful publishing tool for technical writers that solves the problem of collaboratively authoring & managing documentation.”</blockquote>
                                        <blockquote>“StreamRoot is a smart peer-to-peer video streaming technology that reduces by 70% the bandwidth costs for web video platforms.”</blockquote>
                                    </div>
                                </li>
                                <li>
                                    <div class="title">
                                        <h4 class="inline-block mb0">How and when did you meet your cofounders?</h4>
                                    </div>
                                    <div class="content">
                                        <p class="mb0">
                                            Here we’re trying to get an idea of your team as a system and understand the relationship between all of you. The questions we ask ourselves are: have they already worked together and how well do they know each other? How experienced are they in their
                                            respective fields? Do their skills match the project’s challenges? Are the roles clearly defined and is there a natural leader?
                                        </p>
                                        <p class="mb0">
                                            The questions we’re really answering here are: does each member hold an amount of shares proportional to his future role, contribution and responsibilities? How could equity distribution reveal or impact personal involvement in the project?
                                        </p>
                                        <p>
                                            Even if your company is not incorporated yet, you’d probably better lay all cards on the table now and be honest about what’s expected of each member and what they’ll get in return. We’ve seen bad breakups happen after months of hard work because of misaligned
                                            personal assumptions cofounders had about how much value they brought to the project.
                                        </p>
                                    </div>
                                </li>
                                <li>
                                    <div class="title">
                                        <h4 class="inline-block mb0">What is the team equity breakdown? How did you determine it?</h4>
                                    </div>
                                    <div class="content">
                                        <p>
                                            Rather than a simple question, this is an exhortation to have the most uncomfortable conversation with your team while you’re still in time. It’s better to get rid of the elephant in the room now to make sure you’re building a business on solid foundations.
                                        </p>
                                    </div>
                                </li>
                                <li>
                                    <div class="title">
                                        <h4 class="inline-block mb0">Team video</h4>
                                    </div>
                                    <div class="content">
                                        <p class="mb0">
                                            First off: if you’re not a pro of video-making, don’t waste your time trying too hard to become one now, we assure you it is not necessary, what we look at is not the quality of your directing, lightning or acting skills, we just look at you as individuals
                                            with a passion and a team working towards a common objective. There’s just one important thing to be said about quality: make sure the audio is understandable. In fact we recommend you send the video link to
                                            someone outside your team and ask them to repeat what you said in the video, just to make sure you didn’t just waste an hour of your time.
                                        </p>
                                        <h5>Limit the video to 1-2 minutes.</h5>
                                    </div>
                                </li>
                                <li>
                                    <div class="title">
                                        <h4 class="inline-block mb0">What is the problem that you address?</h4>
                                    </div>
                                    <div class="content">
                                        <p class="mb0">
                                            Here we look at how well you understand the pain point you’re working on alleviating, the depth of your empathy with the customer and your ability to detach yourself from the solution you’re building. We ask you not to talk about your solution because
                                            a common mistake entrepreneurs make is to fall in love with their product or technology and forget that what matters is delivering value to customers, not the means you use. A customer rarely cares about how
                                            you solve their problem as long as you do solve it. A solution can rarely fix more than a single problem, but a single problem can be fixed many different ways, that’s why we look for people that are good at
                                            analysing problems first. Tip: your customer’s problem is not the lack of your solution. Try asking yourself or your teammates this question: would you still work to solve this problem if you realized you can’t
                                            use your current or planned technology and tools ? If the answer is no, you might be too attached to the solution.
                                        </p>
                                        <p class="mb0">Example:</p>
                                        <blockquote style="font-size:16px;">“Shipping an item is a pretty annoying experience. Shipping lots of stuff to customers everyday can become a nightmare, and logistics a full-time job. A small hardware startup or online seller have 2 options to
                                            handle shippings : 1) develop its own logistics process internally (super time-consuming and definitely not scalable if they don’t have the growth of Amazon) or 2) outsource everything to a logistician (complex,
                                            minimum volumes required, very costly). People usually start with the first option before switching to a logistician when they are completed swamped under volumes that keep going up. But switching to a logistician
                                            is a very complicated process on top of being super expensive.“
                                        </blockquote>
                                    </div>
                                </li>
                                <li>
                                    <div class="title">
                                        <h4 class="inline-block mb0">How do you solve it?</h4>
                                    </div>
                                    <div class="content">
                                        <p class="mb0">
                                            Convince us that your solution not only perfectly solves the problem you describe, but it will become indispensable to your customers because it provides real value to them.
                                        </p>
                                        <p class="mb0">Example:</p>
                                        <blockquote class="mb0" style="font-size:16px;">
                                            “We solve this issue using machine learning algorithms that are much more precise at forecasting demand than hand-operated solutions. Our technology analyzes millions of data streams that impact future attendance for a movie, from which: movie-related
                                            data (casting popularity, what’s being said about the movie online...) contextual factors (weather, concurrent events…) and the cinema historical data. Then, for each cinema locally, we forecast the number of
                                            entrances for each screening with an X% accuracy, and create optimal weekly schedules that maximizes the occupancy rate. That way, we are able to increase the cinema's revenue up to Y% annually.”
                                        </blockquote>
                                        <p class="mb0">Describe the difference between someone who is your early adopter and someone who could be a potential customer at a later stage.</p>
                                        <p class="mb0">This question is about market segmentation, your customer knowledge and how far you went into researching what makes a client worth pursuing. Among the factors that determine the choice of your primary target customer,
                                            the ones we look at here are the margin of improvement your solution brings them, potential recurrence, your personal affinity or privileged access to the niche of choice and its profitability. Identify the
                                            essential difference between early adopters and that wider audience you might acquire only at a later stage.</p>
                                        <p class="mb0">Example:</p>
                                        <blockquote class="mb0" style="font-size:16px;">
                                            “Our potential early adopters are professionals that need to ship items on a regular basis. Good examples are hardware startups, marketplaces sellers, online stores, samples companies, local stores starting to sell online. Stores looking only to deliver
                                            packages within the same city are not our primary customers, as there is no shipping involved. Individuals could soon be our customers but are not yet targeted, as the recurrence, the volumes and the costs of
                                            acquisition are very different from professionals.”
                                        </blockquote>
                                    </div>
                                </li>
                                <li>
                                    <div class="title">
                                        <h4 class="inline-block mb0">How big is your addressable market?</h4>
                                    </div>
                                    <div class="content">
                                        <p>
                                            Here we’re trying to understand how big the opportunity is, if it is a VC fundable business and if you are able to clearly describe your market. Please provide precise insights on the market, do go into details about how many/percentage of companies/clients
                                            you believe can reach and why! A common mistake is for start-ups to provide big figures to impress readers, such as “The market is huge, European Millennials are forecast to spend over 30 billion euro in online
                                            bookings in 2016 (Phocuswright) and according to Expedia 75% of this population would like a service that would provided recommendations based on their budget and 67% on their Personality. Also Tripadvisor is
                                            growing and trying to really sell trips. The market is huge and personalization and AI is the future.”. By any means, show us that you understand your market and who you’re targeting, provide realistics figures
                                            on how many clients you believe you can reach within 16 months and why you’ve made these assumptions.
                                        </p>
                                    </div>
                                </li>
                                <li>
                                    <div class="title">
                                        <h4 class="inline-block mb0">How do you acquire customers?</h4>
                                    </div>
                                    <div class="content">
                                        <p class="mb0">
                                            Provide insights on the market and how it is currently approached. Do you have an active approach to getting customers or do they come naturally for any reason? Can a good SEO strategy do the trick? Is the adword market already saturated? Is your product
                                            or brand strong enough to attract customers by itself? Will your client’s cost of acquisition be high because competition is fierce and how do you plan to make up for that?
                                        </p>
                                        <p class="mb0">
                                            Who are your direct and indirect competitors? What are your advantages and disadvantages compared to your direct or indirect competitors? Be honest and specific - this question demonstrates your awareness of the market.
                                        </p>
                                        <p class="mb0">
                                            Here we try to understand several things: compare the improvement you solution brings to the customer segment to competing solutions, how crowded the market is, what you’ve learned from researching it, how likely it would be to build a monopoly. The most
                                            common mistake startups make is to say they have no competitors because no-one does exactly what they do: remember it’s not how you solve the problem, it’s the essence of the problem (and possibly the niche)
                                            that puts you in the same playing field with competitors, if something solves the same problem in a suboptimal way, it counts as an alternative solution. Even when you have no competitors to name you’re still
                                            competing with your customer’s way of currently dealing with his problem and you’ll have to think of what will make them overcome the friction of adopting your solution instead. Pretending competitors don’t
                                            exist is counterproductive because we’ll assume that you either haven’t done your research, don’t understand what a competitor is or that you think we don’t know how to use a search engine, either way it looks
                                            bad. If you find it easier to explain your market positioning with a slide you can link to a slide on Slideshare.
                                        </p>
                                    </div>
                                </li>
                                <li>
                                    <div class="title">
                                        <h4 class="inline-block mb0">What best describes your revenue model?</h4>
                                    </div>
                                    <div class="content">
                                        <p class="mb0">
                                            If you are to build a sustainable and hopefully profitable business you may have to answer this question sooner or later. We’d like to challenge you to think about this now and show us you’re capable of looking at your startup as a self-sustainable system
                                            with inputs and outputs, where the value you are capturing is higher than the resources you are expending.
                                        </p>
                                        <p class="mb0">
                                            What has been your progress over the last three months? Please provide figures (KPIs, revenues, user growth, etc…) and explanation for them?
                                        </p>
                                        <p class="mb0">
                                            Show us the evolution of your project over time and how you keep track of your progress. Explain any variation, success and fails if there are any. Show us that you’re quick to iterate to customer feedbacks.
                                        </p>
                                        <p class="mb0">
                                            Your answer gives us an idea of how mature your business is, but more importantly what indicators you’re using to measure progress. Have you heard about the terms vanity metrics? If you haven’t, google it.
                                        </p>
                                    </div>
                                </li>
                                <li>
                                    <div class="title">
                                        <h4 class="inline-block mb0">Will you attend the program on-site and full time?</h4>
                                    </div>
                                    <div class="content">
                                        <p class="mb0">
                                            One of NUMA’s main differentiator is the great community, we strongly encourage the core team of your startup to be present at NUMA the whole time of the program, no matter how well your remote coordination skills are, you’d be missing on an essential
                                            component of the value proposition. With this said, we’ve worked with distributed teams before, but these are more the exception than the rule.
                                        </p>
                                        <p class="mb0">
                                            Wow, you read this whole guide! As a reward, let us give you a final, semi-secret tip: recommendations. At the end of the questionnaire you’ll find a recommendations box, the best type of recommendation you can get is from an alumni of our acceleration
                                            program. If you don’t know anyone from our previous cohorts, it’s a good opportunity to find out more about the program from people who went through it and at the same time get recommended, you can find the
                                            list of alumni here.
                                        </p>
                                        <p class="mb0">
                                            That’s all folks, good luck with your applications! For any further questions please ask for access to the NUMA Paris Apply Slack NUMA Paris Apply Slack or come to one of our open meetings (13th of June, 4th of July, 22nd of August, 12th of September).
                                        </p>
                                    </div>
                                </li>
